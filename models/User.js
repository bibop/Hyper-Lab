const mongoose = require('mongoose');
const Schema = mongoose.Schema;

// User Schema Definition
const UserSchema = new Schema({
  username: {
    type: String,
    required: true,
    unique: true,
    trim: true
  },
  email: {
    type: String,
    required: true,
    unique: true,
    trim: true,
    lowercase: true
  },
  password: {
    type: String,
    required: true
  },
  createdAt: {
    type: Date,
    default: Date.now
  },
  updatedAt: {
    type: Date,
    default: Date.now
  }
});

// Create User Model from the Schema
const User = mongoose.model('User', UserSchema);

module.exports = User;